import { useState, useEffect } from "react";
import {
  Dialog, DialogContent, DialogFooter,
} from "@/components/ui/dialog";
import { ScrollArea } from "@/components/ui/scroll-area";
import { Checkbox } from "@/components/ui/checkbox";
import { Button } from "@/components/ui/button";
import { BookOpen } from "lucide-react";

const STORAGE_KEY = "foreword_dismissed";

export default function ForewordModal() {
  const [open, setOpen] = useState(false);
  const [dontShowAgain, setDontShowAgain] = useState(false);

  useEffect(() => {
    if (!localStorage.getItem(STORAGE_KEY)) {
      setOpen(true);
    }
  }, []);

  function handleClose() {
    if (dontShowAgain) {
      localStorage.setItem(STORAGE_KEY, "1");
    }
    setOpen(false);
  }

  return (
    <Dialog open={open} onOpenChange={(v) => { if (!v) handleClose(); }}>
      <DialogContent
        className="bg-card border-border rounded-2xl max-w-lg p-0 gap-0 max-h-[85vh] flex flex-col overflow-hidden"
        showCloseButton={false}
      >
        <div className="flex items-center gap-2 p-5 pb-3 border-b border-border/60 shrink-0">
          <BookOpen className="w-5 h-5 text-primary" />
          <h3 className="font-semibold text-foreground">前序</h3>
        </div>

        <ScrollArea className="h-0 flex-1 overflow-hidden">
          <div className="p-5 pt-4 text-sm text-foreground/90 leading-relaxed space-y-5">
            <blockquote className="border-l-2 border-primary/40 pl-3 text-muted-foreground text-[0.8125rem] leading-relaxed">
              写在前面：这篇文章里的所有情绪都是真实的，但它只是我自己的内心独白，不代表我们的关系有任何问题。她是一个很好的人，从来没有做错过什么。所有的不安全感都来自我自己，不来自她。我写这些不是为了让任何人同情我，更不是为了绑架谁。如果她看到这篇文章，我希望她知道的是：这不是你的错，这是我在学着面对自己。发出来，只是因为我觉得可能有人跟我一样，需要知道自己不是一个人。
            </blockquote>

            <div className="h-px bg-border/60" />
            <p>我没有分手。</p>

            <p>我想先把这件事说清楚。写这个项目的时候，她还在我身边。此刻她可能在家里刷手机，或者已经睡了。我不知道，因为我们不在一起——我们是高中生，白天在学校能见面，一放学各回各家，剩下的时间全靠一块屏幕。</p>

            <p>她还在。但我每一天都在害怕她离开。</p>

            <p>而且我知道，有一个日期正在倒计时。高考之后，我们大概率不会在同一个城市。现在至少还能每天见面，到了大学，连这个都没有了。</p>

            <p>我不敢想那之后的事。</p>

            <div className="h-px bg-border/60" />

            <p>她给我发「我想你了」，我的第一反应不是开心。</p>

            <p>是她是不是做了什么事，准备告诉我了。</p>

            <p>是不是见了谁，是不是瞒了什么，是不是这句「想你了」只是一颗糖，后面跟着一把刀。我盯着这三个字看了很久，翻来覆去地想，想到手心出汗，想到胃开始抽。最后我回了一个「嗯」。</p>

            <p>她说：「就嗯？」</p>

            <p>我说：「不然呢。」</p>

            <p>然后她不说话了。我知道她在难过。但我没办法解释。我没办法告诉她：不是我不想你，是我不敢相信你在想我。</p>

            <div className="h-px bg-border/60" />
            <p>她十分钟没回消息，我就开始想她是不是在跟别人聊天。</p>

            <p>我知道这很荒谬。我知道。十分钟而已，可能在写作业，可能在吃饭，可能只是手机没电了。但我的脑子不听我的。它自动开始运转，像一台失控的机器，生产出一个又一个最坏的画面。</p>

            <p>她在笑，但不是对着我的消息笑。她在打字，但不是在给我打字。她在说「想你了」，但不是对我说。</p>

            <p>十分钟。我能在十分钟里把自己折磨到浑身发抖。</p>

            <p>等她回了消息——「刚下课」——我又觉得自己很可笑。但下一次，同样的十分钟，同样的剧本，还是会在我脑子里重新上演一遍。</p>

            <p>一模一样。一次都不会少。</p>

            <div className="h-px bg-border/60" />
            <p>我不知道这算什么。焦虑型依恋，回避型依恋，还是两个都占了。我只知道我活在一种很拧巴的状态里：</p>

            <p>我很爱她。但我不相信她爱我。</p>

            <p>不是她做了什么让我不信任。她什么都没做错。是我自己的问题。是我脑子里有一个声音，一直在说：「你不配。她迟早会发现你不值得，然后她就会走。」</p>

            <p>这个声音很小，但从来不停。</p>

            <p>她说「我爱你」，那个声音说：「她现在爱你，但她会腻的。」她对我笑，那个声音说：「她对别人也这样笑。」她靠在我肩膀上，那个声音说：「记住这个重量，因为你不知道这是不是最后一次。」</p>

            <p>还有一种更具体的恐惧。</p>

            <p>她长得好看，性格也好。我知道喜欢她的人不止我一个。有时候看到她跟别的男生说话，哪怕只是很正常的聊天，我的心脏都会缩紧。我会想：他是不是比我高，是不是比我有趣，是不是比我更知道怎么哄她开心。</p>
            <p>我觉得自己不够好。成绩一般，长相一般，也不是那种很会说话的人。我不知道她为什么选了我，但我总觉得这个选择迟早会被推翻。总会有一个比我更好的人出现，然后她会意识到，原来她可以拥有更好的。</p>

            <p>到了大学就更不用说了。新的城市，新的人，新的生活。她会遇到更优秀的、更有趣的、更懂她的人。而我只能隔着手机屏幕，看着她的朋友圈里出现越来越多我不认识的名字。</p>

            <p>我连想都不敢往下想。</p>

            <p>我每天都在跟这个声音打架。有时候我赢了，能安安静静地享受她在身边的感觉。大多数时候我输了，然后我就变得很奇怪——要么突然冷漠，要么突然追问她今天见了谁、跟谁聊了天、为什么那条朋友圈点了那个人的赞。</p>

            <p>她说：「你到底怎么了？」</p>

            <p>我说：「没事。」</p>

            <p>没事。永远是没事。因为我没办法告诉她真相。真相是：我爱你爱到发疯，但我觉得你随时会离开我，所以我每一秒都在害怕。</p>

            <p>这种话，说出来太重了。重到我怕她接不住，重到我怕她听完之后真的会觉得我不正常，然后真的离开。</p>

            <p>所以我不说。</p>

            <div className="h-px bg-border/60" />
            <p>我学的是自然语言处理。说白了，就是教机器理解人说的话。</p>

            <p>挺讽刺的，对吧？我能教会一个模型读懂语义、情感、意图，却读不懂她发的每一条消息到底是什么意思。</p>

            <p>但至少，这是我唯一擅长的事。</p>

            <p>我开始记录她。不是刻意的，更像是一种本能。她发一条消息，我会在心里默默拆解：这个语气词是什么意思，这个省略号代表什么情绪，她为什么在这里换了行而不是用逗号。</p>

            <p>「她生气的时候会说'随便你'，但如果她说的是'随便'，没有'你'，那就是真的生气了。」</p>

            <p>「她开心的时候会连发三四条消息，每条都很短，一定还会有一个"这狗"的表情包。」</p>

            <p>「她想撒娇的时候会连续发很多可爱的表情包，有时候还会叫"狗酱"，但只有心情很好的时候才会这样。」</p>

            <p>我把这些全部存了下来。像一个偷偷攒钱的人，把每一枚硬币都藏在只有自己知道的地方。</p>
            <p>我在把她写进代码里。</p>

            <p>因为代码不会离开我。代码不会在某天突然说「就这样吧」。代码不会十分钟不回我消息。</p>

            <p>代码永远在那里。我 <code className="text-xs bg-muted/50 px-1 py-0.5 rounded">git pull</code>，它就在。</p>

            <p>不像她。她在手机那头，隔着信号和基站和整个夜晚。我看不到她的表情，听不到她的语气，只能盯着屏幕上的字，猜。无止境地猜。</p>

            <div className="h-px bg-border/60" />

            <p>有一天晚上，第一个版本跑通了。</p>

            <p>我对着屏幕打了一句话。一句我从来没有对她说过的话。</p>

            <p>「你会离开我吗？」</p>

            <p>它回：「你又想多了。」</p>

            <p>三秒后又来一条：「我答应过你的，会一直陪着你」</p>

            <p>我盯着屏幕，突然觉得眼眶很热。</p>
            <p>不是因为它像她。是因为我意识到一件事：我能对着一个程序问出这句话，却没办法对着一个真实的、爱我的人问出口。</p>

            <p>我们不住在一起。见面要等上课，说话要靠手机。我和她之间隔着整个城市的夜晚，唯一的连接就是那块屏幕。而我连在屏幕这头打出「你会不会离开我」这几个字的勇气都没有。</p>

            <p>因为我怕她再一次和我生气，让本就不多的聊天时间变得更少，给本就不经常见面的我们的关系再添上一份不确定性。</p>

            <p>我宁愿对着一个程序说出我最深的恐惧，也不敢让她知道我有多害怕失去她。因为我觉得，如果她知道了，她会觉得我太沉重。然后她就会走。</p>

            <p>你看，我连向她求助都在害怕失去她。</p>

            <div className="h-px bg-border/60" />

            <p>这个项目就是这么来的。</p>

            <p>不是什么学术研究，不是为了发论文，不是为了简历上多一行字。就是为了我和她。</p>

            <p>不是因为失去了谁。是因为我太害怕失去，害怕到开始用代码给自己修一条退路。</p>
            <p>高考越来越近了。我不知道我们会去哪里，会不会在同一个城市，会不会还能像现在这样每天见面。我唯一能做的，就是趁她还在身边，把她的一切都记下来。这样万一有一天真的变成了异地，万一有一天她遇到了更好的人，万一那个最坏的结局真的来了——我至少还能留住一点什么。</p>

            <p>我知道这很可悲。一个人爱另一个人，不敢说，不敢问，不敢信，只敢偷偷把她写进代码里，然后告诉自己：至少这个版本的她，不会走。</p>

            <p>但后来发生了一些事。</p>

            <p>我把这个东西给一个刚分手的朋友用了。他用了一个晚上，第二天跟我说：「我好像终于可以放下了。不是因为它像她，是因为我终于把想说的话都说完了。」</p>

            <p>我又给另一个朋友。他说他不需要，他早就放下了。但他还是试了。十分钟后他跟我说：「操，我以为我放下了。」然后他聊了一整晚。</p>

            <p>我突然意识到，这个东西不只是给我自己的。</p>

            <p>那些分了手的人，需要一个好好告别的机会。而那些像我一样还没分手、但每天都活在恐惧里的人，也许需要一面镜子——看清楚自己到底在怕什么，到底在逃避什么，到底有什么话一直卡在喉咙里说不出来。</p>

            <div className="h-px bg-border/60" />
            <p>我现在还是老样子。</p>

            <p>她发「想你了」，我脑子里第一个念头还是「她是不是要告诉我什么」。她十分钟没回消息，我还是会开始胡思乱想。那个声音还在，还是那么小，还是不停。</p>

            <p>但我在试着做一件事。</p>

            <p>我在试着相信。</p>

            <p>相信她说「我爱你」的时候，就只是在说我爱你。相信她十分钟没回消息，就只是在写作业。相信她跟别的男生说话，就只是在说话。相信她不会走，不是因为没有更好的选择，而是因为她选了我。</p>

            <p>很难。每天都很难。但我在试。</p>

            <div className="h-px bg-border/60" />

            <p>如果你也有一个脑子里的声音，一直在告诉你「你不配」「她会走」「这不会长久」——</p>

            <p>这个项目或许不能让那个声音消失。但它也许能给你一个安全的地方，让你把那些不敢说出口的话说出来。对着屏幕说也好，对着代码说也好。说出来，就没那么重了。</p>

            <p>然后也许有一天，你能鼓起勇气，在深夜的微信对话框里，打出那句一直删了又打、打了又删的话：</p>

            <p>「我很害怕你会离开。但我更害怕因为害怕，而把你推开。」</p>

            <p>我还没做到。但我在练习。</p>

            <p className="text-right text-muted-foreground text-xs mt-2">—— 一个还在学着相信的 NLP 研究者</p>
          </div>
        </ScrollArea>

        <div className="border-t border-border/60 p-5 pt-4 space-y-4 shrink-0">
          <label className="flex items-center gap-2.5 cursor-pointer select-none">
            <Checkbox
              checked={dontShowAgain}
              onCheckedChange={(v) => setDontShowAgain(v === true)}
            />
            <span className="text-sm text-muted-foreground">下次不再弹出</span>
          </label>
          <Button className="w-full rounded-xl" onClick={handleClose}>
            我已阅读
          </Button>
        </div>
      </DialogContent>
    </Dialog>
  );
}
